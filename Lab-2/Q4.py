import math

# a) 5 to the power of 8
result_a = 5 ** 8
print(f"a) 5^8 = {result_a}")

# b) Square root of 400
result_b = math.sqrt(400)
print(f"b) Square root of 400 = {result_b}")

# c) Exponent of 5 (5^5)
result_c = 5 ** 5
print(f"c) 5^5 = {result_c}")

# d) Logarithm of 625 base 5
result_d = math.log(625, 5)
print(f"d) Log base 5 of 625 = {result_d}")
